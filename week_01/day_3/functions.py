def greet(name,time_of_the_day):
    return "Good " + time_of_the_day + " "+name

name1 = "John"
time_of_the_day_1 = "Morning"

print(greet(name1,time_of_the_day_1))

name2 = "George"
time_of_the_day_2 = "Evening"

print(greet(name2,time_of_the_day_2))

name3 = "Nick"
time_of_the_day_3 = "Night"

print(greet(name3,time_of_the_day_3))
